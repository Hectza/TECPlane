    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        if(row < 0){
            JOptionPane.showMessageDialog(null, "Debe seleccionar una puerta para cerrarla.");
            return;
        }
        if (jTable1.getValueAt(row, 4).equals("No")){
            JOptionPane.showMessageDialog(null, "El vuelo no está abordando. Imposible cerrarlo.");
            return;
        }
        if (jTable1.getValueAt(row, 4).equals("N/A")){
            JOptionPane.showMessageDialog(null, "La puerta no tiene un vuelo asignado.");
            return;
        }
        logic.Gate seleccionada = logic.NewMain.obtenerPuerta((int)jTable1.getValueAt(row, 0));
        if (seleccionada == null){
            return;
        }
        if(seleccionada.getColaEconomica().size() > 0 || seleccionada.getColaPlatino().size() > 0 || seleccionada.getColaOro().size() > 0 || seleccionada.getColaEspecial().size() > 0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog (null, "Aún hay personas en la cola. ¿Cerrar?","Alerta",dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                seleccionada.cerrar();
            }
        }
        else{
            seleccionada.cerrar();
        }
        this.actualizar();                                   
    }//GEN-LAST:event_jButton3ActionPerformed

