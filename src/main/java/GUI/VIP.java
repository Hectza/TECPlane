    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        logic.Node temp = logic.NewMain.puertas.getHead().getNext();
        while(temp != null){
            logic.Gate puerta = (logic.Gate)temp.getElement();
            int numPuerta = puerta.getIdentificador();
            if (puerta.vueloActual == null){
                Object[] row = {numPuerta, "N/A", "N/A", "N/A", "N/A"};
                model = (DefaultTableModel) jTable1.getModel();
                model.addRow(row);
                temp = temp.getNext();
                continue;
            }
            int numVuelo = puerta.vueloActual.getNumero();
            String origen = puerta.vueloActual.getOrigen();
            String destino = puerta.vueloActual.getDestino();
            boolean isBoarding = puerta.isBoarding();
            if(isBoarding){
                Object[] row = {numPuerta, numVuelo, origen, destino, "Sí"};
                model = (DefaultTableModel) jTable1.getModel();
                model.addRow(row);
            }
            else{
                Object[] row = {numPuerta, numVuelo, origen, destino, "No"};
                model = (DefaultTableModel) jTable1.getModel();
                model.addRow(row);
            }
            temp = temp.getNext();
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
