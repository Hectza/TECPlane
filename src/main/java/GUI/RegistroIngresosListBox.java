    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        long pasaporte;
        String nombre = jTextField1.getText();
        if (nombre.equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese un nombre válido.");
            return;
        }
        String fechaNat = jTextField2.getText();;
        if (fechaNat.equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese su fecha de nacimiento.");
            return;
        }
        try{
        pasaporte = Long.parseLong(jTextField3.getText());
        } catch (Exception ie){
            JOptionPane.showMessageDialog(null, "El pasaporte es inválido.");
            return;
        }
        String nacionalidad = jTextField4.getText();
        if (nacionalidad.equals("")){
            JOptionPane.showMessageDialog(null, "Ingrese su nacionalidad.");
            return;
        }
        
        int cond = this.condicion;
        
        int index = jComboBox1.getSelectedIndex();
        logic.NewMain.puertas.goToPos(index);  
        logic.Gate puerta = (logic.Gate)logic.NewMain.puertas.getElement();
        String origen = puerta.vueloActual.getOrigen();
        String destino = puerta.vueloActual.getDestino();
        
        logic.NewMain.checkIn(nombre, nacionalidad, fechaNat, pasaporte, origen, destino, cond);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

