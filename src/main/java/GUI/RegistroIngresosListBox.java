    public void actualizar(){
        for (logic.Node temp = logic.NewMain.puertas.getHead().getNext(); temp != null; temp = temp.getNext()){
            logic.Gate puerta = (logic.Gate)temp.getElement();
            logic.Vuelo vuelo = puerta.vueloActual;
            String origen = vuelo.getOrigen();
            String destino = vuelo.getDestino();
            String toAdd = origen + " > " + destino;
            this.jComboBox1.addItem(toAdd);
        }
    }
